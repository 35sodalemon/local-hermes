def translate(text: str) -> str:
    """将英文文本翻译为中文。

    1. 先查静态字典（精确匹配）
    2. 再查 f-string 模板正则（模糊匹配已格式化的字符串）
    3. 都找不到则返回原文
    """
    if not text:
        return text
    # 静态精确匹配
    if text in _ZH:
        return _ZH[text]
    # f-string 反向匹配
    for pattern, translated in _FSTRING_PATTERNS:
        m = pattern.fullmatch(text)
        if m:
            # 将捕获组按顺序替换回翻译文本中的 {}
            result = translated
            for group in m.groups():
                result = result.replace("{}", group, 1)
            return result
    return text


# Monkey-patch 内置 print，使裸 print() 调用也自动翻译
_original_print = builtins.print


def _patched_print(*args, **kwargs):
    """包装内置 print，对字符串参数自动翻译。"""
    if args and len(args) == 1 and isinstance(args[0], str):
        translated = translate(args[0])
        if translated != args[0]:
            _original_print(translated, **kwargs)
            return
    _original_print(*args, **kwargs)


def install_print_patch():
    """安装 print monkey-patch。在 CLI 入口调用一次。"""
    builtins.print = _patched_print


def uninstall_print_patch():
    """恢复原始 print。"""
    builtins.print = _original_print


