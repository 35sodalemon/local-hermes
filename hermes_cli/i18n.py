def translate(text: str) -> str:
    """将英文文本翻译为中文。

    1. 先查静态字典（精确匹配）
    2. 再查 f-string 模板正则（模糊匹配已格式化的字符串）
    3. 都找不到则返回原文
    """
    if not text:
        return text
    # 静态精确匹配
    if text in _ZH:
        return _ZH[text]
    # f-string 反向匹配
    for pattern, translated in _FSTRING_PATTERNS:
        m = pattern.fullmatch(text)
        if m:
            # 将捕获组按顺序替换回翻译文本中的 {}
            result = translated
            for group in m.groups():
                result = result.replace("{}", group, 1)
            return result
    return text